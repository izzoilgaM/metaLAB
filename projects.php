					<h2>Digital Ecologies</h2>
					<h1>Mapping landscapes dynamically in space and time</h1>
					<p><a id="go_button" href="http://metalab.harvard.edu/digital-ecologies/"></a><br>Digital Ecologies is a collaborative 
					initiative started in summer 2012 with Harvard’s Arnold Arboretum. At its core is a shared passion for exploring past, 
					present and potential relationships between digital tools and human-environment interactions, particularly human-landscape and human-plant (human-woody plant) 
					interactions. Supported by seed funding from the Provost’s Office and the Academic Technology Group, the initiative is plural and evolving, a hybrid of digital 
					development, experimental pedagogy, critical inquiry, community building and expressive invention. This hybrid quality is essential to metaLAB’s approach to the 
					project: it implies, for instance, that as we work on imagining digital tools—together called “Trellis”, things like open databases, participatory mapping, virtual 
					collecting—we keep a critical watch over the contexts and stakes attending this work relative to sites like digital cultures, the history of botanical gardens and 
					the public faces of science—and that we look to expanding on the questions that arise with students in our spring 2013 seminar of the same name. Likewise we hope
					that our project can facilitate emergent ecologies, helping build unanticipated community connections and providing opportunities for expressive offshoots, be they 
					spontaneous, educational or commissioned.</p>
